function applyLanguage(lang) {
  const copy = translations[lang] || translations.en;
  html.lang = lang;
  html.dir = lang === "en" ? "ltr" : "rtl";
  languageSelect.value = lang;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = copy[node.dataset.i18n] || "";
  });

  document.querySelectorAll("[data-i18n-html]").forEach((node) => {
    node.innerHTML = copy[node.dataset.i18nHtml] || "";
  });

  document.querySelectorAll("[data-i18n-list]").forEach((node) => {
    node.innerHTML = "";
    (copy[node.dataset.i18nList] || []).forEach((item) => {
      const li = document.createElement("li");
      li.textContent = item;
      node.append(li);
    });
  });

  storage.set("portfolio-language", lang);
}

function renderProjects(filter = "all") {
  const grid = document.querySelector("#projectGrid");
  grid.innerHTML = "";

  projects
    .filter((project) => filter === "all" || project.type === filter)
    .forEach((project) => {
      const article = document.createElement("article");
      article.className = "project-card";
      article.dataset.surface = "";
      const modelMarkup = project.model
        ? `<model-viewer src="${project.model}" camera-controls touch-action="pan-y" auto-rotate ar shadow-intensity="1" aria-label="${project.title} 3D model"></model-viewer>`
        : `<div class="model-placeholder" aria-hidden="true"><span>${project.type === "printing" ? "GLB" : project.type === "electronics" ? "IoT" : "PY"}</span></div>`;

      article.innerHTML = `
        ${modelMarkup}
        <div class="project-body">
          <div class="project-meta">
            <span>${project.type}</span>
            <span>${project.status}</span>
          </div>
          <h3>${project.title}</h3>
          <p>${project.summary}</p>
          <div class="tag-row">${project.tags.map((tag) => `<span>${tag}</span>`).join("")}</div>
          <a href="${project.link || "#"}" target="${project.link?.startsWith("http") ? "_blank" : "_self"}" rel="noreferrer">Open</a>
        </div>
      `;
      grid.append(article);
    });
}

function renderSkills() {
  const bars = document.querySelector("#skillBars");
  const cloud = document.querySelector("#skillCloud");

  bars.innerHTML = skillBars
    .map(
      ([name, value]) => `
        <div class="skill-bar">
          <div><span>${name}</span><strong>${value}%</strong></div>
          <span class="bar-track"><span style="--value:${value}%"></span></span>
        </div>
      `
    )
    .join("");

  cloud.innerHTML = skillCloud.map((skill) => `<span>${skill}</span>`).join("");
}

function setTheme(theme) {
  html.dataset.theme = theme;
  storage.set("portfolio-theme", theme);
}

function setPreference(name, active) {
  html.classList.toggle(name, active);
  storage.set(`portfolio-${name}`, active ? "1" : "0");
}

languageSelect.addEventListener("change", (event) => applyLanguage(event.target.value));
themeToggle.addEventListener("click", () => setTheme(html.dataset.theme === "light" ? "dark" : "light"));
